package collectionPackage;

import java.util.HashSet;
import java.util.Set;

public class HashSetPractice {
//Hashset implements the set interface with hashtable.
//Hashset cannot hold duplicate value.
//Hashset always hold unique value.
//Hashset doesn't follow the insertion order.
//Hashset allows only one null value.


    public void hashSet() {
        Set<String> students = new HashSet<>();
        students.add("Sumaia");
        students.add("Sadia");
        students.add("Lota");
        students.add("Fayja");

    }
}